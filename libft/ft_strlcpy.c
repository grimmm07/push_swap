#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	b;
	size_t	i;

	i = 0;
	b = 0;
	while (src[b])
		b++;
	if (dstsize == 0)
		return (b);
	while (src[i] != '\0' && i < dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (b);
}
